import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { type IOneCustomerMySuffix } from '@/shared/model/one-customer-my-suffix.model';

const baseApiUrl = 'api/one-customers';

export default class OneCustomerMySuffixService {
  public find(id: string): Promise<IOneCustomerMySuffix> {
    return new Promise<IOneCustomerMySuffix>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: string): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IOneCustomerMySuffix): Promise<IOneCustomerMySuffix> {
    return new Promise<IOneCustomerMySuffix>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IOneCustomerMySuffix): Promise<IOneCustomerMySuffix> {
    return new Promise<IOneCustomerMySuffix>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IOneCustomerMySuffix): Promise<IOneCustomerMySuffix> {
    return new Promise<IOneCustomerMySuffix>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
